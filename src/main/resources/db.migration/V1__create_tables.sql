CREATE TABLE T_AGROCARE_VETERINARIO (
    ID_VETERINARIO INT GENERATED BY DEFAULT AS IDENTITY(START WITH 1 INCREMENT BY 1),
    NOME_VETERINARIO VARCHAR2(90) NOT NULL,
    NUM_CRMV VARCHAR2(15) NOT NULL,
    DESC_ESPECIALIDADE VARCHAR2(80) NOT NULL,
    NUM_TELEFONE NUMBER(11) NOT NULL,
    EMAIL_VETERINARIO VARCHAR2(100) NOT NULL,
    SENHA_VETERINARIO VARCHAR2(100) NOT NULL,
    CONSTRAINT PK_ID_VETERINARIO PRIMARY KEY (ID_VETERINARIO)
);

CREATE TABLE T_AGROCARE_AGROPECUARISTA (
    ID_AGROPECUARISTA INT GENERATED BY DEFAULT AS IDENTITY(START WITH 1 INCREMENT BY 1),
    NOME_AGROPECUARISTA VARCHAR2(60) NOT NULL,
    NUM_TELEFONE NUMBER(11) NOT NULL,
    DESC_ENDERECO VARCHAR2(70) NOT NULL,
    NUM_ANIMAIS NUMBER(6) NOT NULL,
    EMAIL_AGROPECUARISTA VARCHAR2(90) NOT NULL,
    SENHA_AGROPECUARISTA VARCHAR2(90) NOT NULL,
    CONSTRAINT PK_ID_AGROPECUARISTA PRIMARY KEY(ID_AGROPECUARISTA)
);

CREATE TABLE T_AGROCARE_AVALIACOES (
    ID_AVALIACOES INT GENERATED BY DEFAULT AS IDENTITY(START WITH 1 INCREMENT BY 1),
    ID_VETERINARIO INT NOT NULL,
    ID_AGROPECUARISTA INT NOT NULL,
    QTD_ESTRELAS INT CHECK(QTD_ESTRELAS BETWEEN 1 AND 5) NOT NULL,
    MSG_AVALIACAO VARCHAR2(100),
    DATA_AVALIACAO DATE NOT NULL,
    CONSTRAINT PK_ID_AVALIACOES PRIMARY KEY(ID_AVALIACOES),
    CONSTRAINT FK_ID_VETERINARIO FOREIGN KEY (ID_VETERINARIO) REFERENCES T_AGROCARE_VETERINARIO(ID_VETERINARIO),
    CONSTRAINT FK_ID_AGROPECUARISTA FOREIGN KEY (ID_AGROPECUARISTA) REFERENCES T_AGROCARE_AGROPECUARISTA(ID_AGROPECUARISTA)
);

CREATE TABLE T_AGROCARE_ANIMAIS (
    ID_ANIMAL INT GENERATED BY DEFAULT AS IDENTITY(START WITH 1 INCREMENT BY 1),
    ID_AGROPECUARISTA INT NOT NULL,
    ESPECIE VARCHAR2(30) NOT NULL,
    SEXO VARCHAR2(10) NOT NULL,
    IDADE NUMBER(2) NOT NULL,
    PESO_KG NUMBER(5) NOT NULL,
    HISTORICO_MEDICO VARCHAR2(90) NOT NULL,
    STATUS_SAUDE VARCHAR2(40) NOT NULL,
    STATUS_VACINACAO VARCHAR2(50) NOT NULL,
    CONSTRAINT PK_ID_ANIMAL PRIMARY KEY(ID_ANIMAL),
    CONSTRAINT FK_ID_AGROPECUARISTA_T_ANIMAIS FOREIGN KEY(ID_AGROPECUARISTA) REFERENCES T_AGROCARE_AGROPECUARISTA(ID_AGROPECUARISTA)
);


CREATE TABLE T_AGROCARE_CONSULTAS (
    ID_CONSULTA INT GENERATED BY DEFAULT AS IDENTITY(START WITH 1 INCREMENT BY 1),
    ID_VETERINARIO INT NOT NULL,
    ID_AGROPECUARISTA INT NOT NULL,
    ID_ANIMAL INT NOT NULL,
    DESC_CONSULTA VARCHAR2(130) NOT NULL,
    DESC_OBSERVACAO VARCHAR2(90),
    DATA_CONSULTA Date NOT NULL,
    CONSTRAINT PK_ID_CONSULTA PRIMARY KEY(ID_CONSULTA),
    CONSTRAINT FK_ID_VETERINARIO_T_CONSULTAS FOREIGN KEY (ID_VETERINARIO) REFERENCES T_AGROCARE_VETERINARIO(ID_VETERINARIO),
    CONSTRAINT FK_ID_AGROPECUARISTA_T_CONSULTAS FOREIGN KEY (ID_AGROPECUARISTA) REFERENCES T_AGROCARE_AGROPECUARISTA(ID_AGROPECUARISTA),
    CONSTRAINT FK_ID_ANIMAL_T_CONSULTAS FOREIGN KEY (ID_ANIMAL) REFERENCES T_AGROCARE_ANIMAIS(ID_ANIMAL)
);



CREATE TABLE T_AGROCARE_TRATAMENTO (
    ID_TRATAMENTO INT GENERATED BY DEFAULT AS IDENTITY(START WITH 1 INCREMENT BY 1),
    ID_ANIMAL INT NOT NULL,
    TIPO_TRATAMENTO VARCHAR2(30) NOT NULL,
    NOME_MEDICAMENTO VARCHAR2(40) NOT NULL,
    DOSE_MEDICAMENTO_ML NUMBER(6) NOT NULL,
    DESCRICAO_TRATAMENTO VARCHAR2(100) NOT NULL,
    VIA_ADMINISTRACAO VARCHAR2(30) NOT NULL,
    DURACAO_TRATAMENTO VARCHAR2(20) NOT NULL,
    EFEITO_TRATAMENTO VARCHAR2(90) NOT NULL,
    OBSERVACAO_TRATAMENTO VARCHAR2(90) NOT NULL,
    DATA_TRATAMENTO Date NOT NULL,
    CONSTRAINT PK_ID_TRATAMENTO PRIMARY KEY (ID_TRATAMENTO),
    CONSTRAINT FK_ID_ANIMAL_T_TRATAMENTO FOREIGN KEY (ID_ANIMAL) REFERENCES T_AGROCARE_ANIMAIS(ID_ANIMAL)
);